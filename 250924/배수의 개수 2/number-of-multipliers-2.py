# 홀수의 개수를 저장할 변수를 0으로 초기화합니다.
odd_count = 0

# 10번 반복하여 숫자를 입력받습니다.
for i in range(10):
  # 입력받은 문자열을 정수(int)로 변환합니다.
  num = int(input())
  
  # 숫자가 홀수인지 확인합니다.
  # 2로 나눈 나머지가 1이면 홀수입니다.
  if num % 2 == 1:
    # 홀수이면 카운트를 1 증가시킵니다.
    odd_count += 1

# 10개의 숫자를 모두 확인한 후, 최종 홀수 개수를 출력합니다.
print(odd_count)