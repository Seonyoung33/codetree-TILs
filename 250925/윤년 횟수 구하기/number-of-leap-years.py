# 첫 번째 줄에서 정수 N을 입력받습니다.
N = int(input())

# 윤년의 개수를 셀 변수를 0으로 초기화합니다.
leap_year_count = 0

# 1년부터 N년까지 각 연도를 확인하기 위해 반복합니다.
for year in range(1, N + 1):
    # 윤년의 조건을 확인합니다.
    # 1. 400으로 나누어떨어지거나 (OR)
    # 2. 4로 나누어떨어지면서 동시에 100으로는 나누어떨어지지 않아야 함
    if (year % 400 == 0) or (year % 4 == 0 and year % 100 != 0):
        # 조건에 맞으면 윤년이므로 카운트를 1 증가시킵니다.
        leap_year_count += 1

# 최종적으로 계산된 윤년의 수를 출력합니다.
print(leap_year_count)